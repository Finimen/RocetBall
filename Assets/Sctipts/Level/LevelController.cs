using System.Collections;
using UnityEngine;
using UnityEngine.SceneManagement;

namespace FinimenSniperC
{
    [DisallowMultipleComponent]
    internal class LevelController : MonoBehaviour
    {
        [SerializeField] private LoadScreen ui;

        private const LoadSceneMode loadSceneMode = LoadSceneMode.Single;

        public void ReloadScene()
        {
            SceneManager.LoadScene(SceneManager.GetActiveScene().buildIndex);
        }

        public void LoadNextScene()
        {
            LoadScene(SceneManager.GetActiveScene().buildIndex + 1);
        }

        public void LoadScene(int sceneIndex)
        {
            StartCoroutine(LoadSceneAsync(sceneIndex, loadSceneMode));
        }

        public void LoadScene(string sceneName)
        {
            SceneManager.LoadScene(sceneName, loadSceneMode);
        }

        private IEnumerator LoadSceneAsync(int id, LoadSceneMode loadMode)
        {
            ui.Show();

            yield return new WaitForSeconds(1.75f);

            AsyncOperation loading = SceneManager.LoadSceneAsync(id, loadMode);

            while (!loading.isDone)
            {
                ui.SetProgress(loading.progress);

                yield return null;
            }
        }
    }
}