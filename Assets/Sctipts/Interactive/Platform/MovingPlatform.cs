using DG.Tweening;
using UnityEngine;

namespace FinimenSniperC.Interactive
{
    [DisallowMultipleComponent]
    internal class MovingPlatform : MonoBehaviour
    {
        [SerializeField] private Transform startPoint;
        [SerializeField] private Transform endPoint;

        [SerializeField] private float duration = 5;

        private void Awake()
        {
            transform.position = startPoint.position;

            DOTween.Sequence()
                .Append(transform.DOMove(endPoint.position, duration).SetEase(Ease.InOutQuad))
                .Append(transform.DOMove(startPoint.position, duration).SetEase(Ease.InOutQuad))
                .SetLoops(-1);
        }
    }
}