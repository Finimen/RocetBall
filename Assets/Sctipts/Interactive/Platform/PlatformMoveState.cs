using UnityEngine;

namespace FinimenSniperC
{
    public enum Axis
    {
        X = 0,
        Y = 1,
        Z = 2
    }

    [CreateAssetMenu] internal class PlatformMoveState : PlatformState
    {
        [SerializeField] private Transform target;
        
        [Space(15)]
        [SerializeField] private float moveSpeed;
        [SerializeField] private float minDistance = .1f;

        [SerializeField] private float dependenceSpeedByDistance = 1;

        private float currentSpeed;

        public override void Init(params object[] _params)
        {
            movingPlatform = (Transform)_params[0];
        }

        public override void Enter()
        {
            isFinished = false;
        }

        public override void Update()
        {
            if (Vector3.Distance(target.position, target.position) < minDistance)
            {
                Exit();
            }

            currentSpeed = moveSpeed / (Vector3.Distance(movingPlatform.position, target.position) * dependenceSpeedByDistance);

            movingPlatform.position = Vector3.Lerp(movingPlatform.position, target.position, currentSpeed);
        }

        public override void Exit()
        {
            isFinished = true;
        }
    }
}