using UnityEngine;

namespace FinimenSniperC.Interactive
{
    [DisallowMultipleComponent]
    [RequireComponent(typeof(WindZone))]
    internal class WindZoneParticles : MonoBehaviour
    {
        [SerializeField] private ParticleSystem currentParticleSystem;

        [SerializeField] private float particlesCount;

        [SerializeField] private bool chekInStart;

        private ParticleSystem.EmissionModule currentEmissionModule;

        private void Awake()
        {
            currentEmissionModule = currentParticleSystem.emission;
        }

        private void Start()
        {
            GetComponent<WindZone>().OnForceEnabledChange += ChangeParticlesCount;

            if (chekInStart)
            {
                ChangeParticlesCount(GetComponent<WindZone>().ForceEnabled);
            }
        }

        private void ChangeParticlesCount(bool forceEnabled)
        {
            if (forceEnabled)
            {
                currentEmissionModule.rateOverTime = particlesCount;
            }
            else
            {
                currentEmissionModule.rateOverTime = 0;
            }
        }
    }
}