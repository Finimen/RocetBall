using UnityEngine;
using FinimenSniperC.RollerBall;

namespace FinimenSniperC
{
    [DisallowMultipleComponent]
    internal class TransformSynchronizer : MonoBehaviour
    {
        [SerializeField] private Transform playerTransform;

        [SerializeField] private Vector3 lastTrasnfrorm;

        [SerializeField] private Vector3 delta;

        private void Update()
        {
            delta = transform.position - lastTrasnfrorm;

            lastTrasnfrorm = transform.position;

            if (playerTransform)
            {
                playerTransform.position += delta;
            }
        }

        private void OnTriggerEnter(Collider other)
        {
            if (other.GetComponent<Ball>())
            {
                playerTransform = other.GetComponent<Ball>().transform;
            }
        }

        private void OnTriggerExit(Collider other)
        {
            if (other.GetComponent<Ball>())
            {
                playerTransform = null;
            }
        }
    }
}