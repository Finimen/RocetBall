using UnityEngine;

namespace FinimenSniperC.Particle
{
    [DisallowMultipleComponent]
    [RequireComponent(typeof(ParticleSystem))]
    internal class ParticleDestroyer : MonoBehaviour
    {
        private ParticleSystem particleSystemCurrent;

        private void Awake()
        {
            particleSystemCurrent = GetComponent<ParticleSystem>();
        }

        private void FixedUpdate()
        {
            if (!particleSystemCurrent.isPlaying)
            {
                Destroy(gameObject);
            }
        }
    }
}