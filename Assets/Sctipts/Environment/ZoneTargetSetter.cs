using UnityEngine;
using FinimenSniperC.RollerBall;
using FinimenSniperC.CameraControllers;

namespace FinimenSniperC
{
    [DisallowMultipleComponent]
    [RequireComponent(typeof(Collider))]
    internal class ZoneTargetSetter : MonoBehaviour
    {
        [SerializeField] private Transform cameraTarget;

        private FollowTarget cameraController;

        private Transform lastTarget;

        private void Awake()
        {
            cameraController = FindObjectOfType<FollowTarget>();
        }

        private void OnTriggerEnter(Collider other)
        {
            if (other.gameObject.GetComponent<Ball>())
            {
                lastTarget = cameraController.TargetLook;

                cameraController.SetTargetLook(cameraTarget);
            }
        }

        private void OnTriggerExit(Collider other)
        {
            if (other.gameObject.GetComponent<Ball>())
            {
                cameraController.SetTargetLook(lastTarget);
            }
        }
    }
}