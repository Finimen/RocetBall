using UnityEngine;

namespace FinimenSniperC
{
    [CreateAssetMenu]
    internal class SurfaceData : ScriptableObject
    {
        [SerializeField] private AudioClip[] hitClips;

        [SerializeField] private GameObject particleHit;

        [Range(0,1)]
        [SerializeField] private float frictionForce = 1;

        public AudioClip HitClip { get { return hitClips[Random.Range(0, hitClips.Length)]; } }
        public GameObject ParticleHit { get { return particleHit; } }
        public float FrictionForce { get { return frictionForce; } }
    }
}