using UnityEngine;

namespace FinimenSniperC
{
    [DisallowMultipleComponent]
    [RequireComponent(typeof(Collision))]
    internal class BotParticleContoller : MonoBehaviour
    {
        [SerializeField] private float minRelativeVelocity = 1;

        [SerializeField] private GameObject[] hitParticles;

        private void OnCollisionEnter(Collision other)
        {
            float relativeVelocity = other.relativeVelocity.magnitude;

            if(relativeVelocity > minRelativeVelocity)
            {
                Instantiate(hitParticles[Random.Range(0, hitParticles.Length)],
                    transform.position, Quaternion.identity).transform.LookAt(other.transform);
            }
        }
    }
}