using EZCameraShake;
using UnityEngine;

namespace FinimenSniperC
{
    [DisallowMultipleComponent]
    internal class BallCameraEffector : MonoBehaviour
    {
        [SerializeField] private float minRelativeVelocity = 1f;

        [Space(15)]
        [SerializeField] private float magnitudeMultiplay = .2f;
        [SerializeField] private float roughnessMultiplay = .01f;
        [SerializeField] private float fadeInTimeMultiplay = .01f;
        [SerializeField] private float fadeOutTimeMultiplay = .01f;

        private void OnCollisionEnter(Collision other)
        {
            float relativeVelocity = other.relativeVelocity.magnitude;

            if (relativeVelocity > minRelativeVelocity)
            {
                CameraShaker.Instance.ShakeOnce(relativeVelocity * magnitudeMultiplay,
                    relativeVelocity * roughnessMultiplay,
                    relativeVelocity * fadeInTimeMultiplay,
                    relativeVelocity * fadeOutTimeMultiplay);
            }
        }
    }
}