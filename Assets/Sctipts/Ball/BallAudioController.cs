using FinimenSniperC.RollerBall;
using UnityEngine;

namespace FinimenSniperC
{
    [DisallowMultipleComponent]
    internal class BallAudioController : MonoBehaviour
    {
        [SerializeField] private AudioSource move;

        [SerializeField] private float multiplay = .1f;

        private new Rigidbody rigidbody;
        private Ball ball;

        private void Awake()
        {
            rigidbody = GetComponent<Rigidbody>();
            ball = GetComponent<Ball>();
        }

        private void Update()
        {
            move.volume = ball.IsGround? rigidbody.velocity.magnitude * multiplay : 0;
        }
    }
}