using UnityEngine;
using UnityEngine.UI;

namespace FinimenSniperC
{
    [DisallowMultipleComponent]
    internal class UIController : MonoBehaviour
    {
        [SerializeField] private Button[] winButtons;

        public static UIController Instance { get; private set; }

        private void Awake()
        {
            Instance = this;
        }

        public void ShowWinIcon()
        {
            Debug.Log("WinUIShowed");

            if(winButtons.Length == 0)
            {
                throw new System.Exception("winButtons.Length == 0");
            }

            foreach(Button winButton in winButtons) 
            {
                winButton.gameObject.SetActive(true);
            }
        }
    }
}