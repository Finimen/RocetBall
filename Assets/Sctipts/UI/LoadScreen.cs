using UnityEngine;
using UnityEngine.UI;

namespace FinimenSniperC
{
    [RequireComponent(typeof(Canvas))]
    public class LoadScreen : MonoBehaviour
    {
        [SerializeField] private Image progress;
        
        private Canvas[] otherCanvases;
        private Canvas current;

        private void Awake()
        {
            otherCanvases = FindObjectsOfType<Canvas>();
            current = GetComponent<Canvas>();
        }

        public void SetProgress(float amount)
        {
            progress.fillAmount = amount;
        }

        public void Show()
        {
            foreach(var canvas in otherCanvases)
            {
                if(canvas != current)
                {
                    if(canvas != null)
                    {
                        canvas.gameObject.SetActive(false);
                    }
                }
            }

            GetComponent<UIArrayAlphaController>().SetArrayAlpha(1);
        }
    }
}