using UnityEngine;
using FinimenSniperC.UI;

namespace FinimenSniperC
{
    internal class UIArrayAlphaController : MonoBehaviour
    {
        [SerializeField] private UIColorController[] uiColorControllers;

        [SerializeField] private float alphaStart = 0f;
        [SerializeField] private bool getInChildrens;

        private void Start()
        {
            if (getInChildrens)
            {
                uiColorControllers = GetComponentsInChildren<UIColorController>();
            }

            SetArrayAlpha(alphaStart);
        }

        public void SetArrayAlpha(float alpha)
        {
            foreach (UIColorController colorController in uiColorControllers)
            {
                colorController.SetColorAlpha(alpha);
            }
        }
    }
}