using Assets.Scripts.Settings;
using UnityEngine;
using UnityEngine.UI;

namespace FinimenSniperC.Level
{
    [DisallowMultipleComponent]
    internal class LevelUI : MonoBehaviour
    {
        [SerializeField] private LevelData levelData;

        [Space(15)]
        [SerializeField] private Image lockImage;

        [SerializeField] private LevelUISettings settings;

        private void Awake()
        {
            if (levelData.Completed)
            {
                lockImage.sprite = settings.Completed;

                lockImage.color = settings.ColorCompleted;
            }
            else
            {
                lockImage.sprite = levelData.Unlocked ? settings.Unlocked : settings.Locked;
            }
        }
    }
}