using UnityEngine;
using System.Collections;
using UnityStandardAssets.ImageEffects;

namespace FinimenSniperC.UI
{
    [DisallowMultipleComponent]
    internal class MenuController : MonoBehaviour
    {
        [SerializeField] private BlurOptimized blur;

        [SerializeField] private KeyCode controllButton = KeyCode.Tab;

        [SerializeField] private UIColorController[] uiColorControllers;

        private bool menuState;

        private void Update()
        {
            if (Input.GetKeyDown(controllButton))
            {
                menuState = !menuState;

                foreach(UIColorController colorController in uiColorControllers)
                {
                    colorController.SetColorAlpha(menuState ? 1 : 0);
                }
            }

            if (menuState)
            {
                blur.enabled = true;

                blur.blurSize = Mathf.Lerp(blur.blurSize, 10, .05f);

                Time.timeScale = 0;
            }
            else
            {
                blur.blurSize = Mathf.Lerp(blur.blurSize, 0, .05f);

                Time.timeScale = 1;

                if (blur.blurSize <= 0.01)
                {
                    blur.blurSize = 0;

                    blur.enabled = false;
                }
            }
        }
    }
}