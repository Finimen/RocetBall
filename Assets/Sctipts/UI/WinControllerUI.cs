using UnityEngine;
using UnityEngine.UI;
using FinimenSniperC.Level;

namespace FinimenSniperC
{
    [DisallowMultipleComponent]
    internal class WinControllerUI : MonoBehaviour
    {
        [SerializeField] private Text worldLevelID;

        [SerializeField] private Text canvasLevelID;

        private void Awake()
        {
            WinController levelData = FindObjectOfType<WinController>();

            if (canvasLevelID)
            {
                canvasLevelID.text = levelData.LevelData.name;
            }

            if (worldLevelID)
            {
                worldLevelID.text = levelData.LevelData.name;
            }
        }
    }
}